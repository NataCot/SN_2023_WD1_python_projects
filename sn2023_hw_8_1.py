mood = input("Enter your mood: ")

if mood == "happiness":
    print("It's great to see you happy.")
elif mood == "surprise":
    print("Good heavens! I didn't expect that.")
elif mood == "confused":
    print("Center yourself and firmly state: I don't know what to do, and that is ok!")
elif mood == "worried":
    print("Don't worry, it'll get better.")
elif mood == "angry":
    print("Take a deep breath and relax.")
elif mood == "sad":
    print("Cheer up, there are sunnier days ahead!")
else:
    print("I don't recognize this mood")
